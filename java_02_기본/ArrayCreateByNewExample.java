package java_02_기본;

import java.util.Arrays;

public class ArrayCreateByNewExample {
    public static void main(String[] args) {
        int[] arr1 = new int[3];
        System.out.println(Arrays.toString(arr1));

        String[] arr2 = new String[3];
        System.out.println(Arrays.toString(arr2));
    }
}
